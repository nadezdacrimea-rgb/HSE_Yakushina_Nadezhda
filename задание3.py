# ==================== ЧАСТЬ 1. Математические функции ====================

def factorial(n: int) -> int:
    """Вычисляет факториал числа n (n! = 1*2*...*n)."""
    if n < 0:
        raise ValueError("Факториал определён только для неотрицательных чисел")
    result = 1
    for i in range(2, n + 1):
        result *= i
    return result

def max_of_three(numbers: tuple) -> float:
    """Возвращает наибольшее число из кортежа из трёх чисел."""
    if len(numbers) != 3:
        raise ValueError("Кортеж должен содержать ровно три числа")
    return max(numbers[0], numbers[1], numbers[2])

def right_triangle_area(a: float, b: float) -> float:
    """Вычисляет площадь прямоугольного треугольника по двум катетам."""
    return 0.5 * a * b

