"""
Задание №3: Сумма n + nn + nnn

Пользователь вводит целое число n от 1 до 9.
Программа вычисляет сумму:
    n + nn + nnn
где nn — это двухзначное число, составленное из двух цифр n,
а nnn — трёхзначное число из трёх цифр n.

Пример:
    n = 3 → 3 + 33 + 333 = 369

Алгоритм:
1. Запросить у пользователя число.
2. Проверить, что введена одна цифра от 1 до 9 (можно использовать isdigit() и длину строки).
3. Преобразовать строку в целое число.
4. Сформировать числа nn и nnn: либо через преобразование строк, либо арифметически.
5. Вычислить сумму и вывести результат.
"""

# Запрос ввода у пользователя
user_input = input("Введите число от 1 до 9: ")

# Проверка: строка состоит из цифр и имеет длину 1, и не равна '0'
if user_input.isdigit() and len(user_input) == 1 and user_input != '0':
    n = int(user_input)
    
    # Формируем nn и nnn через строки (простой способ)
    nn = int(str(n) * 2)   # например, '3' * 2 = '33' → 33
    nnn = int(str(n) * 3)  # '3' * 3 = '333' → 333
    
    # Вычисляем сумму
    result = n + nn + nnn
    
    # Вывод результата
    print(f"{n} + {nn} + {nnn} = {result}")
else:
    print("Ошибка: введите одну цифру от 1 до 9.")