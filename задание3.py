def generate_court_header(respondent_data: dict) -> str:
    """
    Генерирует шапку процессуального документа.
    
    Параметры:
    respondent_data (dict): Словарь с данными ответчика и номером дела.
        Ожидаемые ключи:
        - 'case_number' (обязательный) - номер дела, например "А40-123456/2023"
        - 'name' - наименование ответчика (для юрлица) или ФИО (для ИП/физлица)
        - 'inn' - ИНН ответчика
        - 'ogrn' или 'ogrnip' - ОГРН (для юрлица) или ОГРНИП (для ИП)
        - 'address' - адрес ответчика
    
    Возвращает:
    str: Готовая шапка документа.
    """
    # Данные истца (указываем свои)
    plaintiff = {
        "name": "Иванов Иван Иванович",           # Ваши данные
        "inn": "123456789012",
        "ogrnip": "321500123456789",
        "address": "125009, г. Москва, ул. Тверская, 5, кв. 12"
    }
    
    # Извлекаем номер дела и код суда
    case_number = respondent_data.get("case_number")
    if not case_number:
        raise ValueError("В данных ответчика отсутствует номер дела (ключ 'case_number')")
    
    # Код суда - первые три символа номера дела (например, "А40")
    court_code = case_number[:3]
    
    # Выбираем суд из справочника
    court = ARBITRATION_COURTS.get(court_code)
    if not court:
        # Если код не найден, используем суд по умолчанию или выбрасываем ошибку
        raise ValueError(f"Неизвестный код суда: {court_code}. Добавьте его в справочник ARBITRATION_COURTS.")
    
    # Формируем шапку с помощью f-string
    header = f"""
В {court['name']}
Адрес: {court['address']}

Истец: {plaintiff['name']}
ИНН {plaintiff['inn']} ОГРНИП {plaintiff['ogrnip']}
Адрес: {plaintiff['address']}

Ответчик: {respondent_data.get('name', 'Не указано')}
ИНН {respondent_data.get('inn', 'Не указан')} {'ОГРН' if 'ogrn' in respondent_data else 'ОГРНИП'} {respondent_data.get('ogrn', respondent_data.get('ogrnip', 'Не указан'))}
Адрес: {respondent_data.get('address', 'Не указан')}

Номер дела {case_number}
"""
    return header.strip()  # убираем лишние пустые строки в начале и конце

# ==================== ЧАСТЬ 3. Функция для массовой генерации ====================

def generate_all_headers(respondents_list: list) -> None:
    """
    Принимает список словарей с данными ответчиков.
    Для каждого элемента вызывает generate_court_header и выводит результат в консоль.
    """
    for i, respondent in enumerate(respondents_list, 1):
        print(f"\n{'='*60}\nШапка №{i}\n{'='*60}")
        try:
            header = generate_court_header(respondent)
            print(header)
        except Exception as e:
            print(f"Ошибка при генерации шапки для ответчика {i}: {e}")

