"""
Задание №2: Перевод секунд в часы, минуты и секунды

Пользователь вводит время в секундах. Программа должна:
1. Проверить, что введенная строка состоит только из цифр (метод .isdigit()).
2. Если проверка пройдена, преобразовать строку в целое число.
3. Рассчитать количество часов, минут и секунд.
4. Сохранить каждое значение в отдельную переменную.
5. Вывести часы, минуты и секунды отдельно на экран.

Пример:
Ввод: 3665
Вывод: Часы: 1, Минуты: 1, Секунды: 5
"""

# Запрос ввода у пользователя
user_input = input("Введите время в секундах: ")

# Проверка, что ввод состоит только из цифр
if user_input.isdigit():
    total_seconds = int(user_input)  # приведение к целому числу
    
    # Расчет часов, минут и секунд
    hours = total_seconds // 3600
    minutes = (total_seconds % 3600) // 60
    seconds = total_seconds % 60
    
    # Вывод результатов
    print(f"Часы: {hours}")
    print(f"Минуты: {minutes}")
    print(f"Секунды: {seconds}")
else:
    print("Ошибка: введите целое положительное число (только цифры).")
