# 3. Наблюдение за id() для изменяемых и неизменяемых типов
print("--- Часть 3: Анализ id() для разных типов объектов ---")

# Неизменяемые типы (int, float, str, tuple)
print("\n[Неизменяемые типы]")
a = 10
b = 10
print(f"a = {a}, id(a) = {id(a)}")
print(f"b = {b}, id(b) = {id(b)}")
print("a и b ссылаются на один объект? (int кэшируется в определённом диапазоне):", id(a) == id(b))

c = "hello"
d = "hello"
print(f"\nc = '{c}', id(c) = {id(c)}")
print(f"d = '{d}', id(d) = {id(d)}")
print("c и d ссылаются на одну строку? (интернирование строк):", id(c) == id(d))

# Изменяемые типы (list, dict, set)
print("\n[Изменяемые типы]")
list1 = [1, 2, 3]
list2 = [1, 2, 3]
print(f"list1 = {list1}, id(list1) = {id(list1)}")
print(f"list2 = {list2}, id(list2) = {id(list2)}")
print("list1 и list2 — разные объекты, даже с одинаковым содержимым:", id(list1) != id(list2))

# Покажем, что при изменении списка его id не меняется
print("\n--- Изменение изменяемого объекта (список) ---")
lst = [10, 20, 30]
print(f"Исходный список: {lst}, id = {id(lst)}")
lst.append(40)
print(f"После добавления элемента: {lst}, id = {id(lst)} (id не изменился!)")

# А для неизменяемого типа (число) при изменении значения создаётся новый объект
print("\n--- Изменение неизменяемого объекта (число) ---")
x = 100
print(f"x = {x}, id(x) = {id(x)}")
x = x + 1
print(f"x = {x}, id(x) = {id(x)} (id изменился, т.к. создан новый объект)")

